CREATE TABLE ACERTOS (
    NUMSORTEIO INTEGER NOT NULL,
    SORTEIO    INTEGER NOT NULL,
    ACERTO     INTEGER NOT NULL,
    CONTADOR   INTEGER NOT NULL,
    SEQUENCIA  VARCHAR(149),

    CONSTRAINT PK_ACERTOS PRIMARY KEY (NUMSORTEIO, SORTEIO, ACERTO, CONTADOR)
);
COMMIT;

CREATE TABLE SEQUENCIAS (
    SORTEIO     INTEGER NOT NULL,
    NUMSORTEIO  INTEGER NOT NULL,
    CONTADOR    INTEGER NOT NULL,
    SEQUENCIA   VARCHAR(149),
    SEL         CHAR(1),

    CONSTRAINT PK_SEQUENCIAS PRIMARY KEY (SORTEIO, NUMSORTEIO, CONTADOR)
);
COMMIT;

CREATE TABLE SORTEIOS (
    CONTADOR        INTEGER NOT NULL,
    SORTEIO         INTEGER NOT NULL,
    NUMBOLAO        INTEGER NOT NULL,
    FIXOS           VARCHAR(149),
    VARIAVEIS       VARCHAR(149),
    DTBOLAO         TIMESTAMP,
    DTDELETADO      TIMESTAMP,
    DELETADO        CHAR(1),
    DTRESULTADO     TIMESTAMP,
    RESULTADO       VARCHAR(149),

    CONSTRAINT PK_SORTEIOS PRIMARY KEY (CONTADOR, SORTEIO, NUMBOLAO)
);
COMMIT;